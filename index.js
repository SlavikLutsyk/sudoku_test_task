function validSolution(board) {
    if (!Array.isArray(board) || board.length !== 9 || !board.every(row => Array.isArray(row) && row.length === 9)) {
      console.error("Error!");
      return false;
    }
    for (let i = 0; i < 9; i++) {
      if (board[i].includes(0)) return false;
    }
    function isValidLine(line) {
      return line.slice().sort().join('') === '123456789';
    }
    for (let i = 0; i < 9; i++) {
      const row = board[i];
      const column = board.map(row => row[i]);
      if (!isValidLine(row) || !isValidLine(column)) return false;
    }
    for (let row = 0; row < 9; row += 3) {
      for (let col = 0; col < 9; col += 3) {
        const block = [];
        for (let i = 0; i < 3; i++) {
          for (let j = 0; j < 3; j++) {
            block.push(board[row + i][col + j]);
          }
        }
        if (!isValidLine(block)) return false;
      }
    }
    return true;
  }

